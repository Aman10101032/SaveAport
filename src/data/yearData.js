// Генерация фейковых данных для быстрой статистики на карточках
export const generateYearsData = () => {
  const yearsData = {};
  for (let year = 2010; year <= 2025; year++) {
    yearsData[year] = {
      общаяОценка: Math.floor(Math.random() * 30) + 70,
      температура: Math.floor(Math.random() * 10) + 15, 
      осадки: Math.floor(Math.random() * 200) + 300, 
      урожайность: Math.floor(Math.random() * 30) + 70 
    };
  }
  return yearsData;
};